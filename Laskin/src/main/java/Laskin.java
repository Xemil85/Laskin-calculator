    private void jButtonPyyhiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPyyhiActionPerformed

        //length() metodilla saadaan merkkien määrä selville ja voidaan poistaa yksi merkki kerrallaan
        int length = jtxtAlkunaytto.getText().length();
        int number = jtxtAlkunaytto.getText().length() - 1;
        String store;
        
        //StringBuilder pystytään tallentamaan merkkijonoja
        //String store:lla pystytään muuttamaan takaisin doublesta String muotoon.
        if (length > 0) {
            StringBuilder back = new StringBuilder(jtxtAlkunaytto.getText());
            back.deleteCharAt(number);
            store=back.toString();
            jtxtAlkunaytto.setText(store);
        }
    }//GEN-LAST:event_jButtonPyyhiActionPerformed

