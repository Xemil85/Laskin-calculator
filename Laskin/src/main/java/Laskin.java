    // LaskuOperaatio() metodi tekee laskukaavat
    public void laskuOperaatio() {
        switch(laskenta){
            case 1: // Pluslasku
                ans = num + Double.parseDouble(jtxtAlkunaytto.getText());
                jtxtAlkunaytto.setText(Double.toString(ans));
                break;
                
            case 2: // Miinuslasku
                ans = num - Double.parseDouble(jtxtAlkunaytto.getText());
                jtxtAlkunaytto.setText(Double.toString(ans));
                break;
                
            case 3: // Kertolasku
                ans = num * Double.parseDouble(jtxtAlkunaytto.getText());
                jtxtAlkunaytto.setText(Double.toString(ans));
                break;
             
            
            case 4: // Jakolasku
                ans = num / Double.parseDouble(jtxtAlkunaytto.getText());
                jtxtAlkunaytto.setText(Double.toString(ans));
                break; 
            
        } 
                
    }

